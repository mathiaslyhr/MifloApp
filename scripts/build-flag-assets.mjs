// Download real country-flag SVGs (flagpedia / flagcdn.com — free for app use)
// for every nationality + club country in the dataset, rasterize them to small
// PNGs, and emit a committed require-map so the app renders real flags offline
// via a native <Image> (fast; no runtime SVG parsing).
//
//   npm run assets:flags
//
// Outputs:
//   src/games/hattrick/assets/flags/<iso>.png        (bundled images)
//   src/games/hattrick/assets/flags.generated.ts     (require map)
//     export const FLAG_IMAGES: Record<countryName, number>
//
// Keyed by the exact country strings used in the dataset (footballer
// nationalities + Club.country). Add new nations to COUNTRY_ISO below; the
// script fails loudly if a used country has no ISO mapping.
import {mkdirSync, writeFileSync, rmSync} from 'node:fs';
import {resolve} from 'node:path';
import {FOOTBALLERS, CLUBS, root} from './_load-football.mjs';
import art from './lib/art-sources.js';
import {rasterizeFlag} from './lib/art.mjs';
const {COUNTRY_ISO} = art;

// Every distinct country the dataset actually uses.
const used = new Set();
for (const f of FOOTBALLERS) for (const n of f.nationality) used.add(n);
for (const c of CLUBS) used.add(c.country);
const countries = [...used].sort();

const missing = countries.filter(c => !COUNTRY_ISO[c]);
if (missing.length) {
  console.error(
    `✗ No flagcdn ISO code for: ${missing.join(', ')}\n` +
      '  Add them to COUNTRY_ISO in scripts/build-flag-assets.mjs.',
  );
  process.exit(1);
}

async function main() {
  const dir = resolve(root, 'src/games/hattrick/assets');
  const imgDir = resolve(dir, 'flags');
  rmSync(imgDir, {recursive: true, force: true});
  mkdirSync(imgDir, {recursive: true});

  const entries = [];
  for (const country of countries) {
    const iso = COUNTRY_ISO[country];
    const png = await rasterizeFlag(iso);
    writeFileSync(resolve(imgDir, `${iso}.png`), png);
    entries.push([country, iso]);
    console.log(`  ✓ ${country} (${iso}) ${png.length}b`);
  }

  const body = entries
    .map(([country, iso]) => `  ${JSON.stringify(country)}: require('./flags/${iso}.png'),`)
    .join('\n');

  const out = `// AUTO-GENERATED by scripts/build-flag-assets.mjs — do not edit by hand.
// Real country flags (flagpedia.net / flagcdn.com), rasterized + bundled for
// offline use. Values are Metro asset ids usable as <Image source>.
// Regenerate with: npm run assets:flags
/* eslint-disable */
export const FLAG_IMAGES: Record<string, number> = {
${body}
};
`;

  writeFileSync(resolve(dir, 'flags.generated.ts'), out);
  console.log(`✓ ${entries.length} flags → assets/flags/*.png + flags.generated.ts`);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
